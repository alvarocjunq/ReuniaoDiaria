package br.com.consinco.reuniaodiaria.modelos;

public class TabelaRPdeRC {

	private int rp;
	private String aplicacao;
	private int hrsprevista;
	private String codetapa;
	private String analistaAnotou;
	private String anotacao;
	private int rc;
	private String anotacaorc;
		
	
	public String getAnotacaorc() {
		return anotacaorc;
	}
	public void setAnotacaorc(String anotacaorc) {
		this.anotacaorc = anotacaorc;
	}
	public int getRc() {
		return rc;
	}
	public void setRc(int rc) {
		this.rc = rc;
	}
	public String getAnotacao() {
		return anotacao;
	}
	public void setAnotacao(String anotacao) {
		this.anotacao = anotacao;
	}
	public int getRp() {
		return rp;
	}
	public void setRp(int rp) {
		this.rp = rp;
	}
	public String getAplicacao() {
		return aplicacao;
	}
	public void setAplicacao(String aplicacao) {
		this.aplicacao = aplicacao;
	}
	public int getHrsprevista() {
		return hrsprevista;
	}
	public void setHrsprevista(int hrsprevista) {
		this.hrsprevista = hrsprevista;
	}
	public String getCodetapa() {
		return codetapa;
	}
	public void setCodetapa(String codetapa) {
		this.codetapa = codetapa;
	}
	public String getAnalistaAnotou() {
		return analistaAnotou;
	}
	public void setAnalistaAnotou(String analistaAnotou) {
		this.analistaAnotou = analistaAnotou;
	}
		
}
